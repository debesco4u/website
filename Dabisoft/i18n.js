const LANG_NAMES = { en: 'EN', es: 'ES', fr: 'FR', de: 'DE' };
const LANG_FLAGS = { en: '🇬🇧', es: '🇪🇸', fr: '🇫🇷', de: '🇩🇪' };

function getCurrentLang() {
  return localStorage.getItem('dabisoft-lang') || 'en';
}

function setLang(lang) {
  localStorage.setItem('dabisoft-lang', lang);
  applyTranslations(lang);
  updateLangSwitcher(lang);
  document.documentElement.lang = lang;
}

function applyTranslations(lang) {
  const t = TRANSLATIONS[lang] || TRANSLATIONS['en'];
  
  // Update all elements with data-i18n attribute
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (t[key]) {
      if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
        // Don't change value, just placeholder
      } else {
        el.innerHTML = t[key];
      }
    }
  });
  
  // Update placeholders
  document.querySelectorAll('[data-i18n-ph]').forEach(el => {
    const key = el.getAttribute('data-i18n-ph');
    if (t[key]) el.placeholder = t[key];
  });
}

function updateLangSwitcher(lang) {
  const currentBtn = document.querySelector('.lang-current');
  if (currentBtn) {
    currentBtn.innerHTML = LANG_FLAGS[lang] + ' ' + LANG_NAMES[lang] + ' <svg width="10" height="10" viewBox="0 0 10 10" fill="currentColor"><path d="M2 4l3 3 3-3"/></svg>';
  }
  document.querySelectorAll('.lang-option').forEach(opt => {
    opt.classList.toggle('active', opt.dataset.lang === lang);
  });
}

// Initialize on page load
document.addEventListener('DOMContentLoaded', () => {
  const lang = getCurrentLang();
  if (lang !== 'en') {
    applyTranslations(lang);
  }
  updateLangSwitcher(lang);
  document.documentElement.lang = lang;
});
